package de.crasheddevelopment.jpgfileinjector.core.converter;

public class BytesToHEX
{
    // Initialize variables.
    private final char[] hexChars = "0123456789ABCDEF".toCharArray();

    // Method to convert bytes to hex
    public final String convertBytesToHex (byte[] bytes)
    {
        // Initialize variables.
        char[] byteChars = new char[bytes.length * 2];

        for (int x = 0; x < bytes.length; x++)
        {
            int xy = bytes[x] & 0xFF;
            byteChars[x * 2] = hexChars[xy >>> 4];
            byteChars[x * 2 + 1] = hexChars[xy & 0x0F];
        }

        // Returns HEX string.
        return new String(byteChars);
    }
}
